let flatTextbookMap = null;

function normalizeString(str) {
  if (!str) return '';
  return str.toLowerCase()
    .replace(/[^a-z0-9가-힣]/g, '')
    .replace(/starter/g, 'st')
    .replace(/reading/g, 'rd')
    .replace(/writing/g, 'wr')
    .replace(/grammar/g, 'gr')
    .replace(/vocabulary/g, 'vc')
    .replace(/vocab/g, 'vc')
    .replace(/phonics/g, 'ph');
}

function expandBooks(baseString) {
  const expanded = [];
  
  // "Up & Away Phonics 1,2,3,4" 또는 "Nelson Grammar 1A,1B" 같은 패턴 찾기
  // 마지막 단어가 숫자로 시작하거나, 쉼표로 연결된 문자열인 경우
  const match = baseString.match(/^(.*?)(?:\s+|-)([0-9A-Z,\s]+)$/);
  
  if (match) {
    const prefix = match[1].trim();
    const suffixes = match[2].split(',').map(s => s.trim()).filter(Boolean);
    
    // 만약 suffix들이 전부 1~3글자라면 (1, 2, 3, 1A, 1B 등)
    if (suffixes.length > 1 && suffixes.every(s => s.length <= 3)) {
      suffixes.forEach(s => {
        expanded.push(`${prefix} ${s}`);
      });
      expanded.push(prefix);
    } else {
      expanded.push(baseString);
    }
  } else {
    // 쉼표가 붙어있는 경우 강제로 분리 시도 (예: "Nelson Grammar 1A,1B")
    const commaMatch = baseString.match(/^(.*?)(\d+[A-Z]?)(,)(\s*\d+[A-Z]?)+$/);
    if (commaMatch) {
      const prefix = commaMatch[1].trim();
      const parts = baseString.substring(prefix.length).split(',').map(s => s.trim());
      parts.forEach(p => expanded.push(`${prefix} ${p}`));
      expanded.push(prefix);
    } else {
      expanded.push(baseString);
    }
  }
  
  return expanded;
}

function initializeMap() {
  if (flatTextbookMap) return;
  flatTextbookMap = [];
  
  Object.keys(rawTextbookData).forEach(level => {
    rawTextbookData[level].forEach(bookStr => {
      // 괄호 및 불필요한 특수문자 전처리
      let cleanBookStr = bookStr.replace(/[()]/g, '').trim();
      
      const expanded = expandBooks(cleanBookStr);
      expanded.forEach(book => {
        flatTextbookMap.push({
          level,
          original: book,
          normalized: normalizeString(book)
        });
      });
      
      // 원본 문자열도 무조건 추가
      flatTextbookMap.push({
        level,
        original: bookStr,
        normalized: normalizeString(bookStr)
      });
    });
  });
  
  // 중복 제거
  const seen = new Set();
  flatTextbookMap = flatTextbookMap.filter(item => {
    if (seen.has(item.normalized)) return false;
    seen.add(item.normalized);
    return true;
  });
  
  flatTextbookMap.sort((a, b) => b.normalized.length - a.normalized.length);
}

export function findTextbookLevel(searchBookName) {
  if (!searchBookName) return null;
  
  // 0. 원장님 직접 지정 매핑 확인 (LocalStorage)
  if (typeof window !== 'undefined') {
    try {
      const customMapStr = localStorage.getItem('customTextbookLevels');
      if (customMapStr) {
        const customMap = JSON.parse(customMapStr);
        if (customMap[searchBookName]) {
          return {
            level: customMap[searchBookName],
            matchedName: '원장님 직접 지정'
          };
        }
      }
    } catch (e) {
      console.error('Failed to read custom textbook levels', e);
    }
  }
  
  initializeMap();
  
  let normSearch = normalizeString(searchBookName);
  if (!normSearch) return null;
  
  // 검색어에서 불필요한 단어 제거 (단순 매칭 정확도 향상을 위해)
  normSearch = normSearch.replace(/level/g, '').replace(/book/g, '');
  
  const searchNumMatch = searchBookName.match(/\d+[a-zA-Z]?/g) || [];
  
  // 1. 완전 일치 또는 포함 관계 검사
  for (const item of flatTextbookMap) {
    if (item.normalized.length < 3) continue;
    
    const itemNumMatch = item.original.match(/\d+[a-zA-Z]?/g) || [];
    
    // 숫자가 포함된 교재의 경우, 숫자가 일치하는지 반드시 확인
    let numMatches = true;
    if (searchNumMatch.length > 0 && itemNumMatch.length > 0) {
      // 검색어의 숫자 중 하나라도 타겟 교재의 숫자 목록에 없으면 (단, 타겟 교재가 숫자가 있을 때만)
      if (!searchNumMatch.some(n => itemNumMatch.includes(n))) {
        numMatches = false;
      }
    } else if (searchNumMatch.length > 0 && itemNumMatch.length === 0) {
      // 검색어에는 숫자가 있는데 타겟에는 숫자가 아예 없다면 (예: Bricks 1 -> Bricks)
      // 레벨이 여러 개 있을 수 있으므로 매칭 확률 낮음. 
      // 하지만 시리즈 자체 매칭을 허용할 수도 있음. 일단은 매칭 패스하지 않음.
    }
    
    if (!numMatches) continue;
    
    if (normSearch.includes(item.normalized) || item.normalized.includes(normSearch)) {
      const isNumberMatchOnly = /^[0-9]+$/.test(item.normalized);
      if (!isNumberMatchOnly) {
        return {
          level: item.level,
          matchedName: item.original
        };
      }
    }
  }
  
  // 2. 단어 부분 일치 (Fuzzy)
  let bestMatch = null;
  let maxScore = 0;
  
  for (const item of flatTextbookMap) {
    const itemNumMatch = item.original.match(/\d+[a-zA-Z]?/g) || [];
    
    let numMatches = true;
    if (searchNumMatch.length > 0 && itemNumMatch.length > 0) {
      if (!searchNumMatch.some(n => itemNumMatch.includes(n))) {
        numMatches = false;
      }
    }
    
    if (!numMatches) continue;
    
    let overlap = 0;
    const s1 = normSearch;
    const s2 = item.normalized;
    
    if (s1.length < 3 || s2.length < 3) continue;
    
    for (let i = 0; i <= s1.length - 3; i++) {
      const chunk = s1.substr(i, 3);
      if (s2.includes(chunk)) overlap++;
    }
    
    const score = overlap / Math.max(s1.length, s2.length);
    if (score > maxScore && score > 0.4) {
      maxScore = score;
      bestMatch = item;
    }
  }
  
  if (bestMatch) {
    return {
      level: bestMatch.level,
      matchedName: bestMatch.original
    };
  }
  
  return null;
}
